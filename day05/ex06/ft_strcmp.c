int		ft_strcmp(char *s1, char *s2)
{
	char *s1_buf;
	char *s2_buf;

	s1_buf = s1;
	s2_buf = s2;
	while (*s1_buf == *s2_buf)
	{
		if (!*s1_buf)
			return (0);
		s1_buf++;
		s2_buf++;
	}
	return (*s1_buf - *s2_buf);
}
